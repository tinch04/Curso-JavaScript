// Sentencias Switch

let nota = 5;

switch (nota) {
    case 5:
        console.log("Buen trabajo, sobresalente");
        break;
    case 4:
        console.log("Buen trabajo, pero podrias haberlo hecho mejor");
        break;

    case 3:
        console.log("has obtenido un sufieciente");
        break;

    case 2:
        console.log("No has aprobado por poco");
        break;

    case 1:
        console.log("no has estudiado nada, trabaja un poquito mas para la proxima");
        break;

    default:
        console.log("error");
        break;
}

